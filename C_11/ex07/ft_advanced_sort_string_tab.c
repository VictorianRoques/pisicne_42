void	ft_advanced_sort_string_tab(char **tab, int (*cmp)(char *, char *))
{
	int i;
	int j;
	char*tmp;
	int len;

	len = 0;
	i = 0;
	while (tab[len])
		len++;
	while (tab[i])
	{
		j = 0;
		while (j < len - 1)
		{
			if (cmp(tab[j], tab[j + 1]) > 0)
			{
				tmp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = tmp;
			}
			j++;
		}
		i++;
	}
}
