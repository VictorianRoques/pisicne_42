#include <unistd.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

int		main(int ac, char **argv)
{
	ac = 1;
	write(1, argv[0], ft_strlen(argv[0]));
	write(1, "\n", 1);
}
